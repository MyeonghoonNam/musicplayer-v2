import Head from 'next/head';
import { ReactElement } from 'react';
import { useRouter } from 'next/router';
import { RootLayout } from '@/components';

import { BackButton, MusicCover, MusicTitle } from './components';
import { usePlayMusic } from './hooks';

import * as Styled from './styled';

const PlayPage = () => {
  const { query } = useRouter();
  const { data: music } = usePlayMusic(query.id as string);

  if (!music) return null;

  return (
    <>
      <Head>
        <title>MusicPlayer: Play</title>
        <meta name="description" content="Generated by hoon" />
        <meta name="viewport" content="width=device-width, initial-scale=1" />
      </Head>

      <Styled.Container>
        <BackButton />
        <MusicCover source={music.cover} />

        <Styled.Contents>
          <MusicTitle title={music.title} />
        </Styled.Contents>
      </Styled.Container>
    </>
  );
};

PlayPage.getLayout = (page: ReactElement) => {
  return <RootLayout>{page}</RootLayout>;
};

export default PlayPage;
